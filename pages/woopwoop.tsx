import type { NextPage } from 'next'
import Head from 'next/head'
import { useEffect, useState } from 'react'
import styled, { keyframes } from 'styled-components'
import { EasterEgg } from '../components/EasterEgg'
import { Fireworks } from '../components/Fireworks'
import { saveSvgAsPng } from 'save-svg-as-png'
import { getEggSeed } from '../utils/eggs'
import Link from 'next/link'
import { trackGoal } from 'fathom-client'

const WoopWoop: NextPage = () => {
  const [showEasterEgg, setShowEasterEgg] = useState(false)
  const [seed] = useState(() => {
    const eggSeed = getEggSeed()

    let seed = 0
    if (eggSeed?.a) seed += eggSeed.a
    if (eggSeed?.b) seed += eggSeed.b
    if (eggSeed?.c) seed += eggSeed.c

    return seed
  })

  useEffect(() => {
    if (seed === 0) {
      window.location.href = '/'
    }

    setShowEasterEgg(true)
  }, [seed])

  const saveEgg = () => {
    saveSvgAsPng(document.getElementById('easter-egg-svg'), 'egg.png')
    trackGoal('VVH0PNHY', 0)
  }

  return (
    <Wrapper>
      <Head>
        <title>ðŸ¤«</title>
        <meta name="robots" content="noindex" />
      </Head>
      <Content>
        {showEasterEgg && (
          <>
            <Title>CONGRATS!</Title>
            <Subtitle>
              You found your own <strong>personal</strong> easter egg! It&apos;s
              been generated by your unique way of finding it and won&apos;t
              look the same for anyone else!
            </Subtitle>
            <EggWrapper>
              <EasterEgg seed={seed} />
            </EggWrapper>
            <Button onClick={saveEgg}>Save your egg</Button>
            <BackLink href="/">Back to start</BackLink>
            <Fireworks />
          </>
        )}
      </Content>
    </Wrapper>
  )
}

